/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    public int kthSmallest(TreeNode root, int k) {
        if(root==null||k<=0){
            return 0;
        }
        int cnt = 0;
        Stack<TreeNode> st = new Stack<>();
        while (root!=null || !st.isEmpty()){
            while (root!=null){
                st.push(root);
                root = root.left;
            }
            cnt = cnt +1;
            root = st.pop();
            if(cnt == k){
                return root.val;
            }
            root = root.right;
        }
        return 0;
    }
}
